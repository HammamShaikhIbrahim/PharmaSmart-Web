<?php
// ==========================================
// إعدادات الاتصال بقاعدة البيانات
// ==========================================


// عنوان السيرفر (غالباً يكون localhost في بيئة التطوير المحلية XAMPP)
$host = 'localhost';

// اسم المستخدم لقاعدة البيانات (الافتراضي هو root)
$user = 'root';

// كلمة المرور (الافتراضية فارغة في XAMPP)
$pass = '';

// اسم قاعدة البيانات التي أنشأناها (تأكد من مطابقتها في phpMyAdmin)
$dbname = 'pharmasmart_db';

// 1. محاولة إنشاء الاتصال باستخدام مكتبة mysqli
$conn = mysqli_connect($host, $user, $pass, $dbname);

// 2. التحقق من نجاح الاتصال
// إذا فشل الاتصال (!conn)، نوقف السكربت فوراً ونطبع رسالة الخطأ
if (!$conn) {
    die("فشل الاتصال بقاعدة البيانات: " . mysqli_connect_error());
}


// 3. ضبط ترميز المحارف (Encoding)
// هذا السطر ضروري جداً لدعم اللغة العربية في البيانات وعدم ظهورها كرموز غريبة (؟؟؟؟)
mysqli_set_charset($conn, "utf8mb4");
